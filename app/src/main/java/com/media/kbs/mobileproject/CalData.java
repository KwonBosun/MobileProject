package com.media.kbs.mobileproject;

public class CalData {
    public double breakfastCal;
    public double lunchCal;
    public double dinnerCal;
    public double snackCal;
    public double waterCal;
    public double exerciseCal;

    public double getBreakfastCal() {
        return breakfastCal;
    }

    public void setBreakfastCal(double breakfastCal) {
        this.breakfastCal = breakfastCal;
    }

    public double getLunchCal() {
        return lunchCal;
    }

    public void setLunchCal(double lunchCal) {
        this.lunchCal = lunchCal;
    }

    public double getDinnerCal() {
        return dinnerCal;
    }

    public void setDinnerCal(double dinnerCal) {
        this.dinnerCal = dinnerCal;
    }

    public double getSnackCal() {
        return snackCal;
    }

    public void setSnackCal(double snackCal) {
        this.snackCal = snackCal;
    }

    public double getWaterCal() {
        return waterCal;
    }

    public void setWaterCal(double waterCal) {
        this.waterCal = waterCal;
    }

    public double getExerciseCal() {
        return exerciseCal;
    }

    public void setExerciseCal(double exerciseCal) {
        this.exerciseCal = exerciseCal;
    }
}
